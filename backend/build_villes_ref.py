def resolve_special(ville: str):
    """Gère Paris/Marseille/Lyon arrondissements + variantes DOM-TOM."""
    # Paris arrondissements: PARIS 1, PARIS 01, PARIS 16, PARIS 1ER, PARIS 2EME...
    m = re.match(r'^PARIS\s+(\d{1,2})(?:E[MR]E?|ER)?$', ville)
    if m:
        n = int(m.group(1))
        if 1 <= n <= 20:
            lat, lon = PARIS_GPS.get(n, (48.8566, 2.3522))
            return ("75", lat, lon)
    # Marseille arrondissements
    m = re.match(r'^MARSEILLE\s+(\d{1,2})(?:E[MR]E?|ER)?$', ville)
    if m:
        n = int(m.group(1))
        if 1 <= n <= 16:
            lat, lon = MARSEILLE_GPS.get(n, (43.2965, 5.3698))
            return ("13", lat, lon)
    # Lyon arrondissements
    m = re.match(r'^LYON\s+(\d)(?:E[MR]E?|ER)?$', ville)
    if m:
        n = int(m.group(1))
        if 1 <= n <= 9:
            lat, lon = LYON_GPS.get(n, (45.7640, 4.8357))
            return ("69", lat, lon)
    # SAINT-DENIS disambiguation: 93 vs 974 → on laisse 974 car plus fréquent en tennis
    # (mais c'est ambigu, on garde la Réunion par défaut dans le dict)
    return None


def build_villes_ref(db_path):
    c = sqlite3.connect(db_path)
    c.execute("PRAGMA foreign_keys=OFF")

    # Créer villes_ref
    c.executescript("""
    CREATE TABLE IF NOT EXISTS villes_ref (
        id              INTEGER PRIMARY KEY AUTOINCREMENT,
        ville           TEXT NOT NULL UNIQUE,
        dep_code        TEXT,
        dep_nom         TEXT,
        region_nom      TEXT,
        lat             REAL,
        lon             REAL
    );
    CREATE INDEX IF NOT EXISTS idx_villes_ref_ville ON villes_ref(ville);
    """)
    c.commit()
    print("Table villes_ref créée")

    # Récupérer toutes les villes distinctes de joueurs
    villes = [r[0] for r in c.execute(
        "SELECT DISTINCT ville FROM joueurs WHERE ville IS NOT NULL AND ville != ''"
    ).fetchall()]
    print(f"Villes à résoudre: {len(villes)}")

    matched = 0
    rows_to_insert = []

    for ville in villes:
        dep_code = lat = lon = None

        # 1. Lookup direct dans le dict
        entry = VILLES.get(ville)
        if entry:
            dep_code, lat, lon = entry
        else:
            # 2. Cas spéciaux (arrondissements)
            special = resolve_special(ville)
            if special:
                dep_code, lat, lon = special

        dep_nom = None
        region_nom = None
        if dep_code and dep_code in DEPS:
            dep_nom, reg_code = DEPS[dep_code]
            region_nom = REGIONS.get(reg_code)

        rows_to_insert.append((ville, dep_code, dep_nom, region_nom, lat, lon))
        if dep_code:
            matched += 1

    # Insert
    c.executemany(
        "INSERT OR IGNORE INTO villes_ref (ville, dep_code, dep_nom, region_nom, lat, lon) VALUES (?,?,?,?,?,?)",
        rows_to_insert
    )
    c.commit()
    print(f"villes_ref: {len(rows_to_insert)} entrées insérées, {matched} avec département")

    # ─── Ajouter colonnes departement/region à joueurs ─────────────────────────
    for col, typ in [("departement", "TEXT"), ("region", "TEXT")]:
        try:
            c.execute(f"ALTER TABLE joueurs ADD COLUMN {col} {typ}")
            print(f"  Colonne joueurs.{col} ajoutée")
        except Exception as e:
            print(f"  {col}: {e}")
    c.commit()

    # Remplir depuis villes_ref
    c.execute("""
        UPDATE joueurs SET
            departement = (SELECT dep_nom  FROM villes_ref WHERE villes_ref.ville = joueurs.ville),
            region      = (SELECT region_nom FROM villes_ref WHERE villes_ref.ville = joueurs.ville)
        WHERE ville IS NOT NULL AND ville != ''
    """)
    n_dept = c.execute("SELECT changes()").fetchone()[0]
    c.commit()
    print(f"  joueurs.departement/region remplis: {n_dept:,} lignes")

    # Stats
    dept_filled = c.execute("SELECT COUNT(*) FROM joueurs WHERE departement IS NOT NULL").fetchone()[0]
    total_avec_ville = c.execute("SELECT COUNT(*) FROM joueurs WHERE ville IS NOT NULL AND ville != ''").fetchone()[0]
    print(f"\n  Couverture département: {dept_filled:,}/{total_avec_ville:,} = {dept_filled/total_avec_ville*100:.1f}%")

    regions = c.execute("SELECT region, COUNT(*) FROM joueurs WHERE region IS NOT NULL GROUP BY region ORDER BY 2 DESC").fetchall()
    print("\n  Répartition par région:")
    for reg, n in regions:
        print(f"    {reg}: {n:,}")

    c.execute("CREATE INDEX IF NOT EXISTS idx_joueurs_dep ON joueurs(departement)")
    c.execute("CREATE INDEX IF NOT EXISTS idx_joueurs_reg ON joueurs(region)")
    c.commit()
    print("\nIndex créés. Terminé.")
    c.close()


